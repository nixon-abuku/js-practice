function add(a, b){
    return a + b;
}

function subtract(a, b){
    return a - b;
}
function multiply(a, b){
    return a * b;
}
function divide(a, b){
    return a / b;
}
const addition = add (10, 5);
const subtraction = subtract (10, 5);
const multiplication = multiply(10, 5);
const division = divide(10, 5);

console.log(`10 + 5 = ${addition}`);
console.log(`10 - 5 = ${subtraction}`);
console.log(`10 * 5 = ${multiplication}`);
console.log(`10 / 5 = ${division}`);
